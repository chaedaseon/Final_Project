package com.test.mvc;

public class HostDTO
{
	// 주요 속성 구성(Host)
	private String hoCode, hoId, hoPw, hoName, hoSsn, hoTel, hoEmail, hoDate;

	// getter / setter 구성
	public String getHoCode()
	{
		return hoCode;
	}

	public void setHoCode(String hoCode)
	{
		this.hoCode = hoCode;
	}

	public String getHoId()
	{
		return hoId;
	}

	public void setHoId(String hoId)
	{
		this.hoId = hoId;
	}

	public String getHoPw()
	{
		return hoPw;
	}

	public void setHoPw(String hoPw)
	{
		this.hoPw = hoPw;
	}

	public String getHoName()
	{
		return hoName;
	}

	public void setHoName(String hoName)
	{
		this.hoName = hoName;
	}

	public String getHoSsn()
	{
		return hoSsn;
	}

	public void setHoSsn(String hoSsn)
	{
		this.hoSsn = hoSsn;
	}

	public String getHoTel()
	{
		return hoTel;
	}

	public void setHoTel(String hoTel)
	{
		this.hoTel = hoTel;
	}

	public String getHoEmail()
	{
		return hoEmail;
	}

	public void setHoEmail(String hoEmail)
	{
		this.hoEmail = hoEmail;
	}

	public String getHoDate()
	{
		return hoDate;
	}

	public void setHoDate(String hoDate)
	{
		this.hoDate = hoDate;
	}
	
}
